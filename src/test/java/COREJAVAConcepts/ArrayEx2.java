package COREJAVAConcepts;

public class ArrayEx2 {

	public static void main(String[] args) {
		     //0, 1, 2, 3  4  5
	int A[] = {10,20,30,60,70,33,66};  //Array Length : 6 Items/ Elements
	
	//To count number of elements in Array 
	System.out.println(A.length);  //6

	//I want to print all the items in the array:  We can use loops
	for (int i = 0; i<A.length; i++) {
		System.out.println(A[i]);  //10,20,30,40,50,60
	}

	//Using While loops and Do-loops print the arrays..
	
		
	}

}
