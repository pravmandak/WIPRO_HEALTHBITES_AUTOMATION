package COREJAVAConcepts;

public class JoinsExample {

	public static void main(String[] args) {
	
	   String Fruits [] = new String[4];
	   
	   Fruits[0] = "Apple";
	   Fruits[1] = "Banana";
	   Fruits[2] = "Carrot";
	   Fruits[3] = "Mango";
  
	   System.out.println(Fruits[0].concat(Fruits[1]).concat(Fruits[2]).concat(Fruits[3]));

	}

}
